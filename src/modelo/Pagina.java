package modelo;

public class Pagina{

	/**
	 * Um objeto do tipo página possui apenas uma String que possui o caminho do
	 * arquivo que tem o conteúdo da página
	 */
	String arquivoConteudo;

	public String getArquivoConteudo() {
		return arquivoConteudo;
	}

	public void setArquivoConteudo(String arquivoConteudo) {
		this.arquivoConteudo = arquivoConteudo;
	}

}
